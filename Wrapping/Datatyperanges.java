package Datatyperanges;

public class Datatyperanges {

	public static void main(String[] args) {
		System.out.println("Integer range : Max : " + Integer.MAX_VALUE + " Min : " + Integer.MIN_VALUE );
		System.out.println("Double range : Max : " + Double.MAX_VALUE + " Min : " + Double.MIN_VALUE );
		System.out.println("Float range : Max : " + Float.MAX_VALUE + " Min : " + Float.MIN_VALUE );
		System.out.println("Long range : Max : "+ Long.MAX_VALUE + " Min : " + Long.MIN_VALUE );
		System.out.println("Short range : Max : " + Short.MAX_VALUE + " Min : "+ Short.MIN_VALUE);
		System.out.println("Byte range : Max : "+ Byte.MAX_VALUE + "Min : "+ Byte.MIN_VALUE);
	}

}
